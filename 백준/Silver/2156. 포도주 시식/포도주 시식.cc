int n;
int arr[10001];
int dp[10001][3]; // i번째 까지 마셨을 때 마신 최대의 양

int f(int x, int cnt) {
    if (x == n) return 0;
    if (dp[x][cnt] != -1) return dp[x][cnt];

    int& ret = dp[x][cnt];
    if (cnt < 2) {
        ret = max(ret, arr[x] + f(x + 1, cnt + 1));
        ret = max(ret, f(x + 1, 0));
    } else {
        ret = max(ret, f(x + 1, 0));
    }
    return ret;
}

